public class PontoBidimensional {
    
   	private double x, y;

	public PontoBidimensional() {
		this.x = 0.0;
		this.y = 0.0;
	}

	
	public PontoBidimensional(double px, double py) {
		this.x = px;
		this.y = py;
	}
}
